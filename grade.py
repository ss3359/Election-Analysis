
#User inputs score for the class
grade = int(input("What was your overall percent grade for the course? \n "))

#Determine the grade for the course
if grade > 90: 
    print("Your grade is a A")
elif grade > 80: 
        print("Your grade is a B") 
elif grade > 70: 
        print("Your grade is a C") 
elif grade > 60: 
        print("Your grade is an D") 
else: 
    print("Your grade is a F")                        
        